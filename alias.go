package orbit

import "github.com/gin-gonic/gin"

// RouterGroup 是 gin.RouterGroup 的别名，表示路由组。
// RouterGroup is an alias for gin.RouterGroup, representing a group of routes.
type RouterGroup = gin.RouterGroup

// Context 是 gin.Context 的别名，表示请求上下文。
// Context is an alias for gin.Context, representing the request context.
type Context = gin.Context

// HandlerFunc 是 gin.HandlerFunc 的别名，表示处理 HTTP 请求的函数。
// HandlerFunc is an alias for gin.HandlerFunc, representing a function that handles HTTP requests.
type HandlerFunc = gin.HandlerFunc

// HandlersChain 是 gin.HandlersChain 的别名，表示处理器链。
// HandlersChain is an alias for gin.HandlersChain, representing a chain of handlers.
type HandlersChain = gin.HandlersChain

// Accounts 是 gin.Accounts 的别名，表示账户集合。
// Accounts is an alias for gin.Accounts, representing a collection of accounts.
type Accounts = gin.Accounts
