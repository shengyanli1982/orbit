package orbit

import "github.com/gin-gonic/gin"

// RouterGroup 是 gin.RouterGroup 的别名，表示路由组
type RouterGroup = gin.RouterGroup

// Context 是 gin.Context 的别名，表示请求上下文
type Context = gin.Context

// HandlerFunc 是 gin.HandlerFunc 的别名，表示处理 HTTP 请求的函数
type HandlerFunc = gin.HandlerFunc

// HandlersChain 是 gin.HandlersChain 的别名，表示处理器链
type HandlersChain = gin.HandlersChain

// Accounts 是 gin.Accounts 的别名，表示账户集合
type Accounts = gin.Accounts
